-- 2. Подсчитайте количество дней рождения, которые приходятся на каждый из дней недели. 
-- Следует учесть, что необходимы дни недели текущего года, а не года рождения.

SELECT * FROM users;
-- Запросим дни недели день рождений, в год рождения и в текущий год.
SELECT 
  id, 
  name, 
  birthday_at, 
  DAYNAME(birthday_at) AS daynames, 
  DAYNAME(DATE_FORMAT(birthday_at, '2020-%m-%d')) AS daynames_2020 
FROM 
  users;
-- Подсчитаем количество дней рождений, которые приходятся на каждый из дней недели текущего года.
SELECT 
  DAYNAME(DATE_FORMAT(birthday_at, '2020-%m-%d')) AS daynames_2020,
  COUNT(*)
FROM 
  users 
GROUP BY 
  daynames_2020;


